# WAP to calculate area of triangle and rectangle
print('Area of triangle calculator.---------------------------------------------------------------------------------------------------')
base = int(input("Enter base of triangle: "))            
height = int(input("Enter height of triangle: "))
area_of_3angle = 0.5*base*height                           
print(f'Area of triangle is {area_of_3angle} square units')





print('Area of rectangle calculator.---------------------------------------------------------------------------------------------------')
length = int(input("Enter length of rectangle: "))            
breadth = int(input("Enter breadth of rectangle: "))
area_of_rect = length * breadth                                
print(f'Area of rectangle is {area_of_rect} square units')